// This is the Mongoose Model

const noteSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  creationDate: {
    type: Date,
    default: Date.now,
  },
});

const Note = mongoose.model("note", noteSchema);
module.exports = Note;
